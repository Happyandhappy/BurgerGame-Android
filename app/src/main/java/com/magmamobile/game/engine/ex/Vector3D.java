package com.magmamobile.game.engine.ex;


public class Vector3D
{

    public float x;
    public float y;
    public float z;

    public Vector3D()
    {
    }

    public Vector3D(float f, float f1, float f2)
    {
        x = f;
        y = f1;
        z = f2;
    }

    public void set(float f, float f1, float f2)
    {
        x = f;
        y = f1;
        z = f2;
    }
}
